package herencia;

/**
 *
 * @author hola
 */
public class Animal {
    private String color;
    private String raza;
    private int tamanio;
    private int peso;
    private int edad;

    public String getColor() {
        return color;
    }

    public String getRaza() {
        return raza;
    }

    public int getTamanio() {
        return tamanio;
    }

    public int getPeso() {
        return peso;
    }

    public int getEdad() {
        return edad;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setRaza(String raza) {
        this.raza = raza;
    }

    public void setTamanio(int tamanio) {
        this.tamanio = tamanio;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
